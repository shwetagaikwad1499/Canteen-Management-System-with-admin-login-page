    private void btn_cal1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cal1ActionPerformed
        // TODO add your handling code here:
        num2 = Double.parseDouble(txt_display.getText());

        if (operations == "+")
        {
            result = num1 + num2;
            answers = String.format("%.2f", result);
            txt_display.setText(answers);
        }

        else if (operations == "-")
        {
            result = num1 - num2;
            answers = String.format("%.2f", result);
            txt_display.setText(answers);
        }

        else if (operations == "*")
        {
            result = num1 * num2;
            answers = String.format("%.2f", result);
            txt_display.setText(answers);
        }

        else if (operations == "/")
        {
            result = num1 / num2;
            answers = String.format("%.2f", result);
            txt_display.setText(answers);
        }

        else
        {
            result = num1 % num2;
            answers = String.format("%.2f", result);
            txt_display.setText(answers);
        }
    }//GEN-LAST:event_btn_cal1ActionPerformed

