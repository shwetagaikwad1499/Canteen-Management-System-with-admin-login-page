    private void chk_spicyChotpotiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_chk_spicyChotpotiMouseClicked
        // TODO add your handling code here:

        try
        {
            String sql = "select * from product where product_id=18";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            String price = rs.getString("price");
            String stock = rs.getString("stock");

            double cFood = Double.parseDouble(txt_bill.getText());
            double spicyChotpoti = Double.parseDouble(txt_spicyChotpoti.getText());
            double pspicyChotpoti = Double.parseDouble(price);

            double pStock = Double.parseDouble(stock);

            if(chk_spicyChotpoti.isSelected())
            {
                i[17] = (spicyChotpoti * pspicyChotpoti) + cFood;
                String pFood = String.format("%.2f", i[17]);
                txt_bill.setText(pFood);

                double nStock = pStock - spicyChotpoti;

                sql = "update product set stock = '"+String.valueOf(nStock)+" ' where product_id=18";

                pst = conn.prepareStatement(sql);
                pst.execute();

            }

        }

        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_chk_spicyChotpotiMouseClicked

